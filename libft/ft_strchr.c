#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	x;

	x = (char) c;
	while (*s != '\0')
	{
		if (*s == x)
		{
			return ((char *)s);
		}
		s++;
	}
	return ((char *) NULL);
}
